# CREATE LINKEDIN POST (DRAFT)
# ==============================
@app.route("/create-post", methods=["POST"])
def create_post():
    """
    Create a LinkedIn post draft.
    Writes to /Inbox per hackathon document architecture.
    
    Correct flow:
    MCP → /Inbox → Orchestrator → /Needs_Action → AI → Plans → /Pending_Approval → Approved → Done
    
    Expected JSON:
    {
        "text": "Post content",
        "title": "Optional title",
        "visibility": "PUBLIC" | "CONNECTIONS"
    }
    """
    data = request.json

    text = data.get("text", "")
    title = data.get("title", "Business Update")
    visibility = data.get("visibility", "PUBLIC")

    if not text:
        return jsonify({
            "status": "error",
            "message": "Post text is required"
        }), 400

    # FIX: Write to /Inbox (NOT /Pending_Approval)
    # Orchestrator will move to /Needs_Action, then AI processes
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    inbox_file = VAULT / "Inbox" / f"LINKEDIN_POST_{timestamp}.md"

    content = f"""---
type: linkedin_post
source: mcp
title: {title}
visibility: {visibility}
status: new
created: {timestamp}
---

# LinkedIn Post Request

**Title:** {title}
**Visibility:** {visibility}
**Created:** {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}

## Post Content

{text}

---

## Note

This post will be processed by the AI Employee:
1. Orchestrator moves it to /Needs_Action
2. AI creates action plan
3. Approval requested if needed
4. Human approves in /Pending_Approval
5. Post published to LinkedIn

*Generated by LinkedIn MCP Server v0.1*
"""

    inbox_file.write_text(content, encoding="utf-8")

    return jsonify({
        "status": "success",
        "message": "LinkedIn post draft created in Inbox",
        "inbox_file": inbox_file.name,
        "note": "Post will be processed through normal workflow: Inbox → Needs_Action → Plans → Approval → Done"
    })


# ==============================
# EXECUTE LINKEDIN POST
# ==============================
@app.route("/execute-post", methods=["POST"])
def execute_post():
    """
    Execute an approved LinkedIn post.
    Called by orchestrator when approval file is moved to /Approved.
    """
    data = request.json
    
    post_content = data.get("text", "")
    access_token = os.getenv("LINKEDIN_ACCESS_TOKEN")
    
    if not access_token:
        return jsonify({
            "status": "error",
            "message": "LinkedIn access token not configured. Set LINKEDIN_ACCESS_TOKEN in .env"
        }), 500
    
    try:
        # Get authenticated user's URN
        headers = {
            "Authorization": f"Bearer {access_token}",
            "Content-Type": "application/json",
            "X-Restli-Protocol-Version": "2.0.0"
        }
        
        # Get person URN
        person_response = requests.get(
            f"{LINKEDIN_API_BASE}/me",
            headers=headers
        )
        
        if person_response.status_code != 200:
            return jsonify({
                "status": "error",
                "message": f"Failed to get user info: {person_response.text}"
            }), 500
        
        person_urn = person_response.json().get("id")
        author_urn = f"urn:li:person:{person_urn}"
        
        # Create post
        post_data = {
            "author": author_urn,
            "lifecycleState": "PUBLISHED",
            "specificContent": {
                "com.linkedin.ugc.ShareContent": {
                    "shareCommentary": {
                        "text": post_content
                    },
                    "shareMediaCategory": "NONE"
                }
            },
            "visibility": {
                "com.linkedin.ugc.MemberNetworkVisibility": "PUBLIC"
            }
        }
        
        post_response = requests.post(
            f"{LINKEDIN_API_BASE}/ugcPosts",
            headers=headers,
            json=post_data
        )
        
        if post_response.status_code == 201:
            post_id = post_response.json().get("id")
            
            # Log successful post
            log_linkedin_post(post_content, post_id, "success")
            
            return jsonify({
                "status": "success",
                "message": "Post published to LinkedIn",
                "post_id": post_id
            })
        else:
            return jsonify({
                "status": "error",
                "message": f"Failed to post: {post_response.text}"
            }), 500
            
    except Exception as e:
        return jsonify({
            "status": "error",
            "message": str(e)
        }), 500


