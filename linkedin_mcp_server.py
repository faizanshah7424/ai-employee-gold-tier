# SCHEDULE LINKEDIN POST
# ==============================
@app.route("/schedule-post", methods=["POST"])
def schedule_post():
    """
    Schedule a LinkedIn post for later.
    Writes to /Inbox per hackathon document architecture.
    
    Expected JSON:
    {
        "text": "Post content",
        "scheduled_time": "2026-01-15T09:00:00"
    }
    """
    data = request.json

    text = data.get("text", "")
    scheduled_time = data.get("scheduled_time", "")

    if not text or not scheduled_time:
        return jsonify({
            "status": "error",
            "message": "Both text and scheduled_time are required"
        }), 400

    # FIX: Write to /Inbox (NOT /Briefings)
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    inbox_file = VAULT / "Inbox" / f"LINKEDIN_SCHEDULED_{timestamp}.md"

    content = f"""---
type: linkedin_post_scheduled
source: mcp
text: {text[:100]}...
scheduled_time: {scheduled_time}
status: new
created: {timestamp}
---

# Scheduled LinkedIn Post Request

**Scheduled Time:** {scheduled_time}
**Created:** {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}

## Post Content

{text}

---

## Note

This scheduled post will be processed through normal workflow.
*Generated by LinkedIn MCP Server v0.1*
"""

    inbox_file.write_text(content, encoding="utf-8")

    return jsonify({
        "status": "success",
        "message": f"LinkedIn post scheduled for {scheduled_time}",
        "inbox_file": inbox_file.name
    })


