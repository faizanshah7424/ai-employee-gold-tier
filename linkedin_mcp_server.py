@app.route("/status", methods=["GET"])
def status():
    """Get LinkedIn MCP server status"""
    # FIX: Check Inbox (correct architecture)
    pending_posts = list((VAULT / "Inbox").glob("LINKEDIN_POST_*.md"))
    scheduled_posts = list((VAULT / "Inbox").glob("LINKEDIN_SCHEDULED_*.md"))

    return jsonify({
        "status": "operational",
        "pending_in_inbox": len(pending_posts),
        "scheduled_in_inbox": len(scheduled_posts),
        "configured": bool(os.getenv("LINKEDIN_ACCESS_TOKEN"))
    })


# ==============================
# LOGGING
# ==============================
def log_linkedin_post(content: str, post_id: str, status: str):
    """Log LinkedIn post to Logs folder"""
    today = datetime.now().strftime("%Y-%m-%d")
    log_file = VAULT / "Logs" / f"{today}.md"
    
    timestamp = datetime.now().isoformat()
    log_entry = f"""
## [{timestamp}] LinkedIn Post
- **Status:** {status}
- **Post ID:** {post_id}
- **Content:** {content[:100]}...

"""
    
    if log_file.exists():
        log_content = log_file.read_text(encoding="utf-8")
    else:
        log_content = f"# Activity Log - {today}\n\n"
    
    log_content += log_entry
    log_file.write_text(log_content, encoding="utf-8")


